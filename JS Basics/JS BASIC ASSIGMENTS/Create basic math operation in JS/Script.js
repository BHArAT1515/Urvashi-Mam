// Addition
const sum = (num1, num2) => num1 + num2;

// Subtraction
const difference = (num1, num2) => num1 - num2;

// Multiplication
const product = (num1, num2) => num1 * num2;

// Division
const quotient = (num1, num2) => num1 / num2;

// Example usage
const num1 = 10;
const num2 = 5;

console.log("Sum:", sum(num1, num2)); // Output: 15
console.log("Difference:", difference(num1, num2)); // Output: 5
console.log("Product:", product(num1, num2)); // Output: 50
console.log("Quotient:", quotient(num1, num2)); // Output: 2
